    <h5>第7章 比赛时长 </h5>
    <p>1. 比赛时间的划分 </p>
    <p>比赛分为两个时间相等的半场，每个45分钟。仅在符合赛事规程，且在比赛开始之前主裁判和双方队伍同意的情况下，比赛时长可以缩短。</p> 
    <p>2. 中场休息 </p>
    <p>队员有中场休息的权利，休息时间不得超过 15 分钟。赛事规程必须注明中场休息的时长，只有经主裁判同意方可调整该时长。</p>
    <p>3. 允许补充的时间 </p>
    <p>在每半场比赛中，就以下其情况所损失的所有时间应由主裁判予以补足:</p>
    <p>- 替换队员</p>
    <p>- 对受伤队员的伤势评估与移出场地</p>
    <p>- 浪费的时间</p>
    <p>- 纪律处罚</p>
    <p>- 赛事规程所允许的用于饮水或其他医疗原因的暂停时间</p>
    <p>- 任何其他原因，包括任何对比赛重新开始的重大延迟(例如进球后的庆祝) </p>
    <p>- 第四官员在每半场最后一分钟结束前展示由主裁判决定的最短补时时长。</p>
    <p>- 裁判员可以增加补时时间，但不得减少补时时间。</p>
    <p>- 主裁判不得因上半场计时出错而改变下半场的时长。</p>
    <p>4. 罚点球 </p>
    <p>如果执行或者重新执行罚点球，每半场结束时间应延长至罚点球结束。</p>
    <p>5. 中止的比赛 </p>
    <p>除赛事规程或组织者另有规定外，中止的比赛应重新进行。</p>

    <h5>第 8 章 比赛开始和重新开始 </h5>
    <p>比赛的上下半场、加时赛上下半场均以开球的方式开始，进球得分后比赛 也以开球的方式重新开始。任意球(直接或间接)、点球、界外球、球门球和 角球是比赛重新开始的其他方式(见规则第 13 至 17 章)。当主裁判停止比赛且竞赛规则未要求以上述方式重新开始比赛时，应以坠球方式重新开始比赛。比赛成死球状态期间发生犯规不改变比赛重新开始的方式。</p>
    <p>1. 开球 </p>
    <p>程序</p> 
    <p>- 掷硬币，猜中一方选择上半场进攻方向,另一方开球 </p>
    <p>- 猜中的一方在下半场开球</p>
    <p>- 下半场两队交换阵地和进攻方向 </p>
    <p>- 一方进球得分后，由另一方开球 </p>
    <p>每次开球: </p>
    <p>- 所有队员必须在本方半场之内</p>
    <p>- 开球队的对方队员必须距球至少 9.15 米(10 码)直到比赛开始 - 球必须放定在球场中点上</p>
    <p>- 主裁判给出信号</p>
    <p>- 当球被踢并且明显移动比赛即进入活球状态 - 开球可以直接射入对方球门得分</p>
    <p>违规与判罚</p>
    <p>如果开球队员在其他队员触球前再次触球，由对方罚间接任意球，或者在其故意手球的情况下由对方罚直接任意球。</p>
    <p>在开球程序上如有其他违规情况，应重新开球。</p></p>
    <p>2. 坠球 </p>
    <p>程序</p> 
    <p>主裁判在比赛停止时球所在地点坠球，除非比赛在球门区内被停止。如果比赛在球门区内被停止，应在与球门线平行的球门区线上、距比赛停止时球所在地点最近的位置坠球。球触地后比赛即进入活球状态。参与坠球的队员人数没有限制(包括守门员);主裁判不能决定参与坠球
    的队员，也不能决定坠球的结果。</p>
    <p>违规与判罚 </p>
    <p>重新坠球: </p>
    <p>- 球在接触地面前被队员触及</p>
    <p>- 球在接触地面后未经队员触及而离开比赛场地 </p>
    <p>如果坠球后，球在没有触及至少两名队员的情况下进入球门: </p>
    <p>- 进入触球队员的对方球门，以球门球重新开始比赛</p>
    <p>- 进入触球队员的本方球门，以角球重新开始比赛</p>

    <h5>第 9 章 比赛进行及停止 </h5>
    <p>1、比赛停止</p>
    <p>下列情况为比赛停止:</p>
    <p>• 当球无论从地面或空中全部越过球门线或者边线时。</p>
    <p>• 当比赛被裁判员停止时。</p>
    <p>2、比赛进行 其他所有时间均为比赛进行中，包括球从比赛官员、球门柱、横梁或角旗杆弹回场内。</p> 
      
    <h5>第 10 章 比赛结果的产生 </h5>
    <p>1. 进球得分 </p>
    <p>当球的整体从球门柱间及横梁下越过球门线，且攻进球的队在进球前未违反竞赛规则，即为进球得分。如果球的整体还没有完全越过球门线，主裁判即用信号示意球进门，应以坠球形式重新开始比赛。</p>
    <p>2. 获胜的队 </p>
    <p>在比赛中进球较多的队为胜者。如两队均未进球或者进球数相等，则比赛
    为平局。当赛事规程要求一场比赛或主客场两回合比赛成平局后决出胜者时，只能
    采取下列程序来决出:</p>
    <p>- 客场进球规则</p>
    <p>- 加时赛</p>
    <p>- 踢点球决胜</p>
    </p> 
    <p>3. 踢点球决胜 </p>
    <p>踢点球决胜在比赛结束后进行。除非另有所述，相关竞赛规则在踢点球决胜时同样适用。
    <p>程序 </p>
    <p>踢点球决胜之前: </p>
    <p>- 除非有其他考虑(如场地条件、安全因素等)</p>，主裁判投币选定用于踢点球的球门。决定后，只可因为安全原因，或者球门、场地状况变得不可用而 更改主裁判再次投币，挑币得胜的球队决定本队是先踢还是后踢
    <p>- 除了替换受伤的守门员上场的替补队员之外，只有比赛结束时在场上的队员 和临时离场(受伤或调整装备等)</p>的队员方可参加踢点球每个队负责从符合资格的队员中挑选踢点球的队员并安排其踢球顺序，此排序不必通知主裁判 
    <p>- 如果比赛结束时，踢点球决胜之前或过程中，某队的队员人数多于另一队，该队必须减去多出的人数以使得双方人数相等，并必须通知主裁判每一名被去除队员的姓名和号码。被去除的队员将不再具有参加踢点球决胜的资格(下一 条款所述的情况除外)</p>
    <p>- 如果踢点球决胜之前或过程中，守门员不能继续比赛且该队的替补名额尚未用完，可以使用被提名的替补队员或因上一条款而被去除的队员替换，被换下 的守门员不得再参加后续的点球决胜过程(包括踢点球)踢点球决胜的过程中: </p>
    <p>- 只允许具备资格的队员和裁判组成员在场地内</p>
    <p>- 除踢点球的队员和两名守门员外，其他具备资格的队员必须留在中圈内</p>
    <p>- 踢点球队的守门员必须处于罚球区以外的球门线与罚球区线交汇处的比赛场 地内</p>
    <p>- 具备资格的队员可以与守门员互换位置</p>
    <p>- 一个点球在以下情况视为完成:皮球不再移动、越出比赛场地、主裁判吹罚 犯规停止比赛</p>
    <p>- 主裁判记录整个踢点球过程 </p>
    受限于下述条件，两队各踢 5 次: 
    <p>- 双方轮流踢</p>
    <p>- 如果在两队均踢满 5 次前，一队的进球数已多于另一队即便踢满 5 次时能射 中的最多球数，则不需再踢</p>
    <p>- 如果两队均已踢满 5 次，双方进球数相同，则继续踢点球，直至双方踢球次 数相同，而一队较另一队多进一球时为止 </p>
    <p>- 每次应由不同的队员踢点球，直至具备资格的队员均踢过一次后，方可有队员踢第二次</p>
    <p>- 后续轮次的踢点球同样适用上述规则，但球队可以改变踢点球队员的顺序</p>
    <p>- 踢点球决胜不得因队员离场而延误，如果离场队员不能及时返回踢点球，该 次踢球取消(视为未踢进) </p>
    <p>踢球点球决胜期间的替换与罚令出场:</p>
    <p>- 队员、替补队员和被替换下场的队员都可以被警告或罚令出场</p>
    <p>- 如果守门员被罚出场，必须由具备资格的队员替换 - 如果守门员之外的队员不能继续比赛，不得被替换 - 如果一个队不足 7 </p>人，主裁判不得中止比赛</p>

    <h5>第11章 越位 </h5>
    <p>1. 越位位置 </p>
    <p>队员处于越位位置本身并不构成犯规。 队员处于越位位置是指:</p>
    <p>- 头、躯干或脚的任何部分在对方半场(不含中线)，且头、躯干或脚的任何部分较球和倒数第二名对方队员更接近于对方球门线
    所有队员(包括守门员)的手和臂部不在上述判定的考虑范围之内。 以下情况，队员不处于越位位置:
    <p>- 齐平于倒数第二名对方队员 </p>
    <p>- 齐平于最后两名对方队员</p> 
    <p>2. 越位犯规 </p>
    <p>在同队队员踢或触及球的一瞬间处于越位位置的队员，仅在其随后以下列方式卷入到现实比赛的情况下才被判为越位犯规:干扰比赛——处理或者触及队友传来或碰触的球，或干扰对方队员:通过明显阻挡对方视线来阻止对方触球或可能的触球，或与对方争抢球，或明显试图去处理距离自己很近的球且此行为影响到对方，或 做出明显的动作，且该动作明显地影响对方处理球的能力或在以下情形，通过触球或者干扰对方队员获得利益:当球从球门柱、横梁、或对方队员身上弹回或变向 ·当球被对方队员有意识地救出处于越位位置的队员，接得对方队员有意识处理的球(对方队员有意识的
    救球除外)，不被视为获得利益。救球是指队员用除手之外身体的任何部分(守门员在罚球区内除外)来阻止即将进入球门的球或者距离球门很近的球。</p>
    <p>3. 不犯规 </p>
    <p>如果队员直接接到: </p>
    <p>- 球门球 </p>
    <p>- 界外球 </p>
    <p>- 角球</p>
    <p>则没有越位犯规。</p>
    <p>4. 违规与判罚 如果发生越位犯规，主裁判判由对方在犯规发生的地点(包括有可能在犯规队员本方半场)踢间接任意球。守方队员如果未经主裁判允许离开比赛场地，就越位判罚而言应视其处于球门线或边线上，直到比赛下一次成死球状态、或守方将球踢向中线且球已离开守方罚球区为止。如果队员故意离开比赛场地，当比赛下一次成死球状态时，主裁判必须对其进行黄牌警告。攻方队员可以离开比赛场地或停留在比赛场地外以避免卷入现实比赛。如果该队员在下一次比赛成死球状态之前、或者球被守方踢向中线且球已离开罚球区之前，从球门线直接进入比赛场地并卷入比赛，则就越位判罚而言，该队员应被视为处于球门线上。如果一名队员故意离开比赛场地且未经主裁判允许重新进入，在不构成越位犯规的情况下因此获得利益，该球员必须被黄牌警告。在球被踢进球门的过程中，如果一名攻方队员在球门柱之间的球门内保持 不动，则必须判进球有效，除非该队员实施越位犯规、或规则第 12 章所述犯 规，在这种情况下，应判罚任意球(直接或者间接)重新开始比赛。
    </p>

    <h5>第 12 章 犯规与不正当行为-1.直接任意球 </h5>
    <p>只有在活球状态下发生的犯规和违例行为，方可被判罚直接任意球、间接任意球和点球。</p>
    <p>1. 直接任意球 </p>
    <p>如果队员以主裁判认为是用草率、鲁莽或使用过分力量的方式对对方队员作出下列任何侵犯行为，将判给对方踢直接任意球:
    <p>- 冲撞 </p>
    <p>- 跳向 </p>
    <p>- 踢或企图踢</p>
    <p>-推</p>
    <p>- 打或企图打(包括用头撞) </p>
    <p>- 争抢</p>
    <p>- 绊摔或企图绊摔 如果侵犯行为发生身体接触，都应判罚直接任意球或点球。</p>
    <p>- 草率，是指队员在争抢时不注意或欠考虑，或者行为没有采取预防措 施。不必给予纪律处罚</p>
    <p>- 鲁莽，是指队员不顾及其行为可能对对方造成的危险或带来的后果。必须给予黄牌警告</p>
    <p>- 使用过分力量，是指队员使用超过必要限度的力量，并且危害了对方的安 </p>全。必须被罚令出场如果队员作出以下任何犯规行为，也判给对方踢直接任意球:
    <p>- 故意手球(守门员在本方罚球区内除外) </p>
    <p>- 拉扯对方队员 </p>
    <p>- 通过身体接触阻挡对方队员 </p>
    <p>- 向对方队员吐口水 </p>
    <p>参见规则第 3 章所述犯规 </p>
    <p>手球</p>
    <p>手球是指队员故意用手或臂部与球接触的行为。 必须考虑如下因素:</p>
    <p>- 手向球的方向移动(不是球向手移动) </p>
    <p>- 对方和球之间的距离(意外的球)</p>
    <p>- 手的位置并不必然意味着构成犯规</p>
    <p>- 用手中的物品(衣物、护腿板等)触球属于手球犯规 </p>
    <p>- 用手掷出物品(球鞋、护腿板等)击球属于手球犯规 </p>
    <p>在罚球区外，守门员在手球方面与其他队员受相同的限制。在本方罚球区内，守门员不会因手球犯规被判罚直接任意球或受到任何相关处罚，但有可能因手球犯规而被判罚间接任意球。</p>

    <h5>第13章 任意球 </h5>
    <p>1. 任意球的种类 </p>
    <p>如果一名队员犯规或违例，对方球队将获得直接任意球或间接任意球。</p>
    间接任意球手势 当主裁判示意间接任意球时，应单臂上举过头，并保持这种姿势直到球被
    踢出后触及其他队员或者进入死球状态。在踢间接任意球时，如果主裁判没有上举手臂示意，而球直接被踢进球
    门，必须重踢。</p>
    <p>球进门</p> 
    <p>- 如果直接任意球直接踢入对方球门，判为得分</p>
    <p>- 如果间接任意球直接踢入对方球门，判为球门球</p>
    <p>- 如果直接任意球或间接任意球直接踢入本方球门，判给对方踢角球 </p>
    <p>2. 程序 </p>
    <p>所有任意球应在犯规发生地点发出，下列情况除外:</p>
    <p>- 攻方在对方球门区内因对方犯规获得的间接任意球，应在与球门线平行 的球门区线上距犯规发生地点最近的位置发出
    <p>- 守方在本方球门区内获得的任意球可以在该区域内任何一点发出
    <p>- 对于未经主裁判许可进入、重新进入或故意离开比赛场地的犯规，任意球应在比赛停止时球所在位置发出。但是，如果队员的离场属于其比赛动作的一部 分，并对其他队员犯规，则任意球应在距离犯规发生地点最近的边界线上发出;如果执行地点在犯规方罚球区内，且为应判罚直接任意球的犯规，则判罚</p>
    <p>点球 </p>
    <p>- 规则指定了其他位置(参见第 3 章、第 11 章、第 12 章) 球:</p> 
    <p>- 必须放稳，且踢球队员在球未触及其他队员前不得再次触球</p>
    <p>- 当被踢且明显移动，比赛即进入活球状态。但对于守方在其罚球区内获得的 任意球，当球被直接踢出罚球区比赛方进入活球状态
    <p>比赛进入活球状态之前，对方队员的位置要求:</p>
    <p>- 距球至少 9.15 米(10 码)，除非已在本方球门柱之间的球门线上 - 当守方在其罚球区内获得任意球时，应处于在该罚球区外任意球允许以单脚或双脚同时将球挑起的方式踢出。佯装踢任意球以迷惑对方是允许的，这是足球的一部分。如果一名队员正确踢出任意球时，故意将球踢向对方以便自己再次触球，只要他没有采用草率、鲁莽的方式或使用过分的力量，主裁判允许比赛继续。</p>
    <p>3. 违规与判罚 </p>
    <p>如果踢任意球时，对方队员比规定距离更接近于球，除掌握有利的情况 外，应重踢;但如果队员快速踢出任意球，而距球不足 9.15 米的防守队员将球 截获，主裁判允许比赛继续。不过，如果对方队员故意阻止快发任意球，其必须因延误比赛重新开始而被黄牌警告。如果守方在本方罚球区内快速踢出任意球时，对方队员因没有足够的时间离开仍然滞留在该罚球区内，主裁判允许比赛继续。如果守方在本方罚球区内踢任意球时，球未被直接踢出罚球区，应重踢。如果比赛进入活球状态之后，踢球队员在其他队员触球前再次触球，判罚
    间接任意球。若其故意用手触球: </p>
    <p>- 判罚直接任意球 </p>
    <p>- 如果犯规地点在踢球队员本方罚球区内，判罚点球;但如果踢球队员是守门 员，则判罚间接任意球</p>

    <h5>第14章 点球</h5> 
    <p>如果队员作出应判罚直接任意球的犯规，且犯规地点在本方罚球区内，或 虽在场外但根据第 12 章和第 13 章的规定属于比赛的一部分，对方将获得点 球。 点球可以直接得分。</p> 
    <p>1. 程序</p>
    <p>球必须放定在罚球点上。必须明确主罚点球的队员。守门员必须停留在球门柱之间的球门线上，面向主罚队员，直到球被踢
    出。 除主罚队员和守门员之外的队员必须处于: </p>
    <p>- 距罚球点至少 9.15 米(10 码)</p> 
    <p>- 罚球点后</p>
    <p>- 比赛场地内</p> 
    <p>- 罚球区外 所有队员根据规则就位后，主裁判给出踢点球信号。主罚队员必须向前踢球;只要球向前移动，用脚后跟踢球是可以的。当球被踢且明显移动时比赛即进入活球状态。在球触及其他队员前主罚队员不得再次触球。当球停止移动、进入死球状态、或主裁判判罚犯规中断比赛时，踢点球即为完成。 在每半场比赛或加时赛上下半场结束时，应允许延长时间踢完点球。</p>
    <p>2. 违规与判罚 </p>
    <p>一旦主裁判给出踢点球信号，球必须被踢出。如果在比赛进入活球状态之前发生下列情况:
    <p>主罚队员或其队友违规:</p> 
    <p>- 如果球进入球门，应重踢
    <p>- 如果球未进入球门，主裁判应停止比赛，以间接任意球重新开始比赛但如果发生下列违规情况，无论是否进球，均须停止比赛，以间接任意球
    重新开始比赛:</p>
    <p>- 向后踢点球</p>
    <p>- 实际踢点球的是指定主罚队员的队友，主裁判须对实际踢球队员予以黄牌警告</p>
    <p>- 主罚队员完成助跑后作踢球假动作(助跑过程中允许使用假动作)，裁判员 须对其予以黄牌警告 </p>
    <p>守门员或其队友违规:</p> 
    <p>- 进入球门，进球有效</p>
    <p>- 如果球未进入球门，应重踢点球;如果守门员犯有违规行为，须予以黄牌警告</p>
    <p>攻守双方均违反竞赛规则，应重踢点球，但一方队员犯有更严重的犯规(例如违规假动作)的情况除外。</p>
    <p>当点球踢出后: 主罚队员在球触及其他队员之前再次触球:判罚间接任意球(若其故意用手触球则判罚直接任意球)</p>
    <p>球在向前移动过程中触及场外因素:</p>
    <p>- 应重踢 球从守门员、横梁或球门柱弹回比赛场地内，然后被场外因素触及:</p> 
    <p>- 主裁判停止比赛</p> 
    <p>- 在被场外因素触及的地点坠球重新开始比赛</p> 
    <p>3. 汇总表</p>
    <p>注:“越界”即违反程序中所述站位要求。</p>
    <h5>第15章 界外球 </h5>
      <p>当球的整体从地面或空中越过边线时，掷界外球判给最后触球队员的对方。掷界外球不能直接进球得分:如果掷界外球直接进入对方球门，判罚球门球。如果掷界外球直接进入本方球门，判对方罚角球。
    </p>
    <p>1、程序 </p>
    <p>在掷出球的一瞬间，掷球队员必须:</p> 
    <p>•面向比赛场地</p>
    <p>•任何一只脚的部分站在边线上或者边线外的场地上。</p> 
    <p>•在球越出边线处将球从头后经头顶用双手掷出。</p> 
    <p>所有对方队员必须站在距离掷界外球地点 2 米(2 码)之外。当球进入比赛场地，比赛即为进行。如果球在进入场地前触到了地面，应由同一队在同一位置重新掷界外球。如果界外球未正确掷出，则由对方掷界外球。如果一名队员正确地掷出界外球，故意将球掷在对方队员身上以便第二次触球，但没有草率的、鲁莽的或使用过分的力量，裁判员允许比赛继续。掷球队员在其他队员触球前不得再次触球。</p>
    <p>2、违规与判罚 </p>
    <p>如果比赛进行后，掷球队员在其他队员触球前再次触球，判罚间接任意球;若其故意用手触球:</p>
    <p>•判罚直接任意球。</p> 
    <p>•如果违例发生地点在掷球队员本方罚球区内，判罚球点球;除非掷球队员 是守门员，则判罚间接任意球。</p> 
    <p>对方队员不正当地阻碍掷球队员或分散其注意力(包括移至距离掷界外球地点 2 米以内)，将因非体育行为被予以警告。如果界外球已掷出，判罚间接 任意球。</p> 
    <p>对于任何其他违反本章规则的，由对方掷界外球。</p>
    <h5>第16章 球门球 </h5>
    <p>当球的整体从地面或空中越过球门线，最后触球者为攻方队员，且未进球
    得分时，应判为球门球。球门球可以直接射入对方球门而得分。如果球离开罚球区后直接进入本方
    球门，判给对方踢角球。</p>
    <p>1、程序 </p>
    <p>• 由守方队员在球门球内的任何一点将球放定后踢出。</p>  
    <p>• 球离开罚球区，比赛即为进行。</p> 
    <p>• 对方必须在罚球区外直到比赛进行。</p> 
    <p>2、违规与判罚 </p>
    <p>如果球没有离开罚球区或者在离开罚球区前触及队员，应重踢。如果比赛进行后，踢球队员在其他队员触球前再次触球，判罚间接任意
    球;若其故意用手触球:</p>
    <p>• 判罚直接任意球。</p>
    <p>• 如果违例发生地点在踢球队员本方罚球区内，判罚球点球;除非踢球队当球门球踢出后未触及其他队员时，如果在罚球区内的对方队员触球<p>或者争抢球，应重踢。球员是守门员，则判罚间接任意球。</p>
    <p>在比赛进行前，如果有队员进入罚球区并与对方发生犯规行为，根据具体情况对犯规队员予以警告或罚令出场，仍以球门球重新开始比赛。</p>
    <p>对于任何其他违反本章规则的，应重踢。</p>
    <h5>第17章 角球 </h5>
    <p>当球的整体从地面或空中越过球门线，最后触球者为守方队员，且未进球得分时，应判为角球。</p>
    <p>角球可以直接射入对方球门而得分。如果球直接进入本方球门，判给对方踢角球。</p>
    <p>1、程序 </p>
    <p>•必须将球放在角球区内。</p>
    <p>•球必须放稳后，由攻方队员踢球。</p>
    <p>•当球被踢且明显移动时比赛即为进行，而无需离开角球区。</p>
    <p>•绝不允许移动角旗杆。</p>
    <p>•对方队员必须在距离角球区至少 9.15 米(10 码)以外，直到比赛进行。</p> 
    <p>2、违规与判罚 </p>
    <p>如果比赛进行后，踢球队员在其他队员触球前再次触球，判罚间接任意球。若其故意用手触球:</p>
    <p>• 判罚直接任意球。</p> 
    <p>• 如果违例发生地点在踢球队员本方罚球区内，判罚球点球;除非踢球队 员是守门员，则判罚间接任意球。</p>
    <p>如果一名队员在正确踢角球时，故意将球踢向对方，以便第二次触球，但没有草率的、鲁莽的或使用过分的力量，裁判员允许比赛继续。对于任何其他违反本章规则的，应重踢。</p>
