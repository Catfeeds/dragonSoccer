    <p class="text-center">足球竞赛规则 2017/18</p> 
    <h5>第1章 比赛场地 </h5>
    <p>1. 场地表面 </p>
    <p>比赛场地必须是纯天然草坪;或者如果赛事规程允许，可以使用纯人造草坪，除非赛事规程允许使用混合系统(天然和人造材料的结合)。人造草坪的颜色必须是绿色。在国际足联所属国家协会代表队之间、或俱乐部之间的国际比赛中使用的人造草坪，球场表面必须符合《国际足联品质规程——人造草坪》或《国际比 赛标准》的要求，除非 IFAB 给予特许。</p> 
    <p>2. 场地标记 </p>
    <p>比赛场地必须是长方形，并且必须用无危险性的连续实线来标明。这些边界线属于它所包围的区域。只有在规则第 1 章中明确规定可以使用的线，才能标画在比赛场地上。 两条较长的边界线为边线，两条较短的线为球门线。连接两条边线的中点即中线，比赛场地被中线划分为两个半场。在中线中点处做一个中心点标记。以中心点为圆心，9.15 米(10 码)为半径画一个圆。在球门线和边线外侧，距角球弧 9.15 米(10 码)处，可以分别做垂直于 球门线和边线的标记。 
    所有线的宽度必须一致且不得超过 12 厘米(5 英寸)，球门线必须与球门柱和横梁的宽度相同。使用人造草坪场地时，场地表面有其他的线是允许的，前提是这些线和足
    球场标线的颜色不同，可以明显做出区分。队员在赛场内做未经许可的标记属于非体育行为，必须予以警告。如果裁
    判员在比赛中发现了这种行为，必须在下一次比赛成死球状态时警告该队员。</p>
    <p>3. 尺寸 </p>
    <p>边线必须长于球门线。长度(边线):最短 90 米(100 码) 最长 120 米(130 码) 宽度(球门线):最短 45 米(50 码) 最长 90 米(100 码) </p>
    <p>4. 尺寸(国际比赛) </p>
    <p>长度:最短 100 米(110 码) 最长 110 米(120 码) 宽度:最短 64 米(70 码) 最长 75 米(80 码)赛事组织者可以在上述范围之内确定球门线和边线的长度。</p>
    <p>5. 球门区 </p>
    <p>从距每个球门柱内侧 5.5 米(6 码)处，画两条垂直于球门线的线。这些 线伸向比赛场地内 5.5 米(6 码)，与一条平行于球门线的线相连接。由这些 线和球门线组成的区域范围是球门区。</p> 
    <p>6. 罚球区 </p>
    <p>从距每个球门柱内侧 16.5 米(18 码)处，画两条垂直于球门线的线。这 些线伸向比赛场地内 16.5 米(18 码)，与一条平行于球门线的线相连接。由 这些线和球门线组成的区域范围是罚球区。在两个罚球区内各画一个罚球点，罚球点距离球门线中点 11 米(12 码)，并与两球门柱距离相等。在两个罚球区外，以罚球点为圆心，9.15 米(10 码)为半径画一段弧。</p>
    <img src="/imgs/ruler2017/1.6.png" width="100%">
    <p>7. 角球区</p> 
    <p>在比赛场地内，以角旗杆为圆心，1 米(1 码)为半径画一个四分之一圆 弧。这段弧线与边线和球门线组成的扇形区域即角球区。 </p>
    <p>8. 旗杆 </p>
    <p>在场地每个角上各竖一根不低于 1.5 米(5 英尺)的平顶旗杆，上系小旗 一面。在中线的两端、边线以外不少于 1 米(1 码)处，也可以放置旗杆，要求 同上。 </p>
    <p>9. 技术区域 </p>
    <p>技术区域是指，对于在体育场内进行的比赛，根据下述描述设置的技术人 员和替补队员专属座席区域。</p>
    <p>技术区域的范围是指</p>
    <p>从替补席两侧向外延伸仅 1 米、向前延伸至距边线 1米的区域</p>
    <p>应该使用标线明确该区域</p>
    <p>允许停留在技术区域内的人数由赛事规程决定</p>
    <p>技术区域内的人员,应在比赛开始前根据赛事规程一一确认</p>
    <img src="/imgs/ruler2017/1.9.png" width="100%">
    <p>必须对自己的行为负责  </p>
    <p>必须留在指定范围内，特殊情况除外，比如当场上队员受伤，理疗师或医 生在裁判员的允许下进入比赛场地 </p>
    <p>同一时间只允许 1 人从技术区域向外传达技战术指导</p>
    <p>10. 球门</p> 
    <p>球门必须置于每条球门线的正中间。</p>
    球门由两根距角旗杆等距离的垂直立柱和连接其顶部的水平横梁组成。球门立柱和横梁由木材、金属或其他被批准的材料制成。其截面形状限于正方形、长方形、圆形或椭圆形，并不得对球员构成危险。两根立柱内侧之间的距离是 7.32 米(8 码)，横梁的下沿距离地面是 2.44 米(8 英尺)。球门柱和球门线的位置关系必须符合下面的图示要求:</p>
    <img src="/imgs/ruler2017/1.10.png" width="100%">
    <p>门柱和横梁具有相同宽度与厚度，且不超过 12 厘米(5 英寸)。如果横梁移位或折断，应停止比赛直至修好复位。如果不能被修复，比赛 必须中止。 不允许用绳子或其他柔软的或有危险性的材料替代横梁。修复后以坠球方式重新恢复比赛。球门后可以挂网，球网系在球门和球门后面的地上，应适当撑起以不影响守门员的活动。</p>
    <p>安全性 球门(包括可移动式球门)必须牢固地固定在地上。</p>
    <p>11. 门线技术(GLT) </p>
    <p>门线技术系统可以用于判断球是否进入球门，为裁判员的决定提供支持。当使用门线技术系统时，可以根据《国际足联品质规程——门线技术》的 相关规定和竞赛规则的要求对球门结构进行改造。门线技术的使用必须符合赛 事规程的规定。 </p>
    <p>门线技术的原则 系统仅用于球门线，唯一作用是判断是否进球。是否进球的指示必须是实时的，并应当在一秒钟内由门线技术系统自动将
    该等指示仅传递给裁判员(把震动和可视信号传递到裁判的手表上)</p>
    <p>门线技术的要求和特殊规定 如果在比赛中使用门线技术系统，赛事组织者必须确保该系统通过以下标准之一的认证:</p>
    <p>- 国际足联专业品质</p>
    <p>- 国际足联品质 - 国际比赛标准 </p>
    <p>独立检测机构必须根据测试手册对不同技术提供商提供的系统的功能和准确性进行确认。如果技术系统达不到测试手册的要求，裁判员不得使用该门线技术系统，并需将事件向相关权威机构报告。当使用球门线技术系统时，裁判员必须在赛前根据《国际足联品质规程—
    —门线技术系统测试手册》测试该系统的功能性。 </p>
    <p>12. 商业广告 </p>
    <p>从球队进入比赛场地至上半场结束离开，下半场重新进入比赛场地至比赛结束，任何商业广告，不管是实际的还是虚拟的，都不允许出现在如下区域: 1比赛场地;2球门网内的地面;3技术区域;4距离场地边界线 1 米(1 码)以内的地面。球门、球门网、旗杆或旗子上不允许出现广告，也不允许安 装任何无关设备(如摄像机、麦克风等)。 </p>
    <p>另外，直立广告必须:距离场地边线 至少 1 米(1 码)、和球门线的距离不少于球门网的纵深 - 距离球门网至少 1 米(1 码)</p>
    <p>13. 标识和徽章 </p>
    <p>在比赛期间，国际足联、洲际足联、国家足协、赛事组委会、俱乐部或其他团体的代表性标志或徽章制品，不管是实际的还是虚拟的，都禁止出现在比赛场地、球门网及其内部区域、球门和旗杆上，仅允许出现在旗杆顶端的旗子上面。</p>

    <h5>第2章 球 </h5>
    <p>1、质量和测量 球必须是: </p>
    <p>•球形。</p>
    <p>•用合适的材料制成。 </p>
    <p>•圆周长度范围:68 厘米(27 英寸)~70 厘米(28 英寸)。</p>
    <p>•比赛开始时的重量介于 410 克(14 盎司)和 450 克(16 盎司)之间。 </p>
    <p>•海平面气压值范围:0.6~1.1个大气压力(600~1100克/平方厘米; 8.5~15.6 磅/平方英寸) </p>
    <p>在国际足联或洲际联合会主办的正式比赛中的球必须具有下列三种标志之一:</p>
    <p>国际足联专业品质 • 国际足联品质 • 国际比赛标准 </p>
    <img src="/imgs/ruler2017/2.1.png" width="100%"> 
    <p>印有上述品质认证标志之前的球，如“国际足联批准”、“国际足联监制”、 “国际比赛用球标准”等，可以在上述赛事中用至 2017 年 7 月。每个标记表明该球已被正式检测，达到符合规则第二章中所阐述的最低标准所规定的特殊技术要求并由国际足球协会理事会审核通过。相关的检测机构也要得到国际足联的批准。当使用球门线技术系统(GLT)时，允许在球体内使用集成技术，但必须 符合上述三项质量标志之一。国家足球协会的比赛必须使用具有符合上述三种标志之一的球。在国际足联、洲际联合会和国家足球协会主办的比赛中，除了比赛及比赛组织者的标志图案和授权厂商的商标外，不允许在球上出现任何商业广告。竞赛规程可以限制此类标志的尺寸和数量。</p>
    <p>2、坏球的更换</p>
    <p>如果球出现破损:</p>
    <p>· 停止比赛;</p>
    <p>· 用更换的球在原球损坏时所在地点坠球重新开始比赛。 </p>
    <p>如果球在开球、球门球、角球、任意球、罚球点球或掷界外球时出现破
    损:</p>
    <p>· 重发/罚。 </p>
    <p>在罚球点球或点球决胜阶段，如果球向前移动后未触及任何队员、横梁或球门柱，球出现破损:</p>
    <p>· 重踢球点球。</p>
    <p>在比赛中未经裁判员许可不得更换球。</p>
    <p>3、备用球 比赛中在场地周边可以码放一些备用球，这些球应符合规则第二章的有关 规定，并在裁判员的控制下使用。</p>
       
    <h5>第3章 队员 </h5> 
    <p>1. 队员人数 一场比赛应有两队参加，每队上场队员最多 11 名，其中必须有 1 名守门 员。如果任何一队少于 7 人则比赛不能开始或继续进行。如果某队因为1名或多名队员故意离开比赛场地而少于7人，裁判员无需立即停止比赛，可以掌握有利情形。但随即比赛成死球后，如果一队在场上不足 7 名队员的最低限额，比赛不得继续进行。 如果赛事规程规定在开球前必须确定所有的队员和替补队员，而某队在开 
    始比赛时场上队员少于 11 人，则只有在赛前上场名单中报名的队员和替补队 员才能在随后到达赛场后参加比赛。 </p>
    <p>2. 替补队员人数 </p>
    <p>正式比赛  在由国际足联、洲际足联或国家足协主办的正式比赛中，每场比赛最多可 以使用3名替补队员。赛事规程必须规定可以有几名替补队员被提名，从 3 名到最多不超过 12 名。</p> 
    <p>其他比赛 在国际 A 级比赛中，最多可以使用 6 名替补队员。其他所有比赛，符合下 列条件即可增加替补队员人数:双方关于替补人数达成一致意见，比赛前通知裁判员。如果赛前没有通知裁判员，或双方未达成一致意见，则每队最多可以使用 6 名替补队员。</p> 
    <p>换下后换上  被换下场的队员重新上场比赛，仅允许在最低级别的比赛(草根/娱乐)中使用，且需经国家足协同意。 </p>
    <p>3. 替补程序</p>
    <p>开赛前必须将替补队员名单提交裁判员，任何未在赛前被提名的替补队员不得参加比赛。</p>
    <p>替换队员时必须遵守以下规定:</p>
    <p>- 替换前必须通知裁判员</p>
    <p>- 被替换队员得到裁判员的允许可以离开比赛场地，除非已经在比赛场地外</p>
    <p>- 被替换队员不要求必须从中线处退场，并不得再次参加该场比赛，除非“换 下后换上”的条款被允许</p>
    <p>- 如果被要求替换下场的队员拒绝离开比赛场地，比赛应继续 </p>
     <p> 替补队员符合以下条件方可入场:</p>
    <p>- 在比赛成死球状态期间</p> 
    <p>- 从中线处</p>
    <p>- 被换下队员离场后</p> 
    <p>- 得到裁判员信号后 </p>
    <p>当替补队员进入比赛场地，替补程序即告完成。从那时起，替补队员成为场上队员，而他所换下的队员成为被换下队员。</p>
    <p>替补队员只要先进入比赛场地，即可由其发球重新开始比赛。</p>
    <p>如果在半场休息或在加时赛之前进行换人，则替补程序应在重新开始比赛之前完成。</p>
    <p>所有替补队员和被换下队员无论上场与否，裁判员均有权对其行使职权。</p>
    <p>4. 更换守门员 场上任何队员都可以与守门员互换位置，前提是: 
    <p>- 互换位置前通知裁判员
    <p>- 在比赛成死球状态时互换</p> 
    <p>5. 违规与判罚 </p>
    <p> 如果一名已提名的替补队员在比赛开始时替换一名上场名单中的队员首发进入比赛场地，而裁判员并未得到通知:</p>
    <p>- 裁判员允许该替补队员继续比赛</p>
    <p>- 不对该替补队员进行纪律处罚</p>
    <p>- 原本上场名单中的队员可以成为提名替补队员 - 不减少该球队的换人名额</p>
    <p>- 裁判员应在主管机构报告此次事件</p> 
    <p>如果一名场上队员与守门员互换位置前未得到裁判员许可: </p>
    <p>- 裁判员允许比赛继续</p>
    <p>- 在下一次比赛成死球状态时，裁判员出示黄牌警告两名队员 任何其他违反本章条款的情况:</p>
    <p>- 裁判员出示黄牌警告相关队员</p> 
    <p>- 在比赛停止时球所在地点由对方队员踢间接任意球重新开始比赛</p> 
    <p>6. 队员和替补队员被罚令出场</p>
    <p>队员被罚令出场:</p> 
    <p>- 如果球队参赛名单尚未提交，该队员不得以任何身份列入参赛名单</p>
    <p>- 如果已列入参赛名单并在开球前被罚下，可以从名单中的替补队员中选一人 替换，替补队员名单不得补充;球队的换人次数不减少</p>
    <p>- 开球后被罚下，不得被替换</p> 
    <p>凡被列入参赛名单的替补队员被罚令出场，无论是在开球前或比赛开始后均不得补充替补队员名单。</p>
    <p>7. 比赛场内多出的人 </p>
    <p>球队名单中注明的教练员和其他官员(队员和替补队员除外)为球队官员。球队名单(队员、替补队员、球队官员)之外的其他人员为场外因素。</p>
    <p>如果球队官员、替补队员、被换下队员、被罚令出场的队员或者场外因素进入比赛场地，裁判员必须:</p>
    <p>- 仅在比赛被干扰时停止比赛 </p>
    <p>- 当比赛停止时，令其离开</p>
    <p>- 处以适当的纪律处罚 </p>
    <p>如果造成比赛停止的干扰是:</p>
    <p>- 来自球队官员、替补队员、被换下队员或被罚令出场的队员，以直接任意球或者点球重新开始比赛</p>
    <p>- 来自场外因素，以坠球重新开始比赛。如果球在进入球门过程中，干扰行为没有阻碍防守队员触球，而且球最终
    进入球门(即便此干扰接触到了球)，判进球有效，除非球进入的是干扰者对手的球门。</p>
    <p>8. 在比赛场地外的队员</p> 
    <p>如果队员在得到裁判员允许离开比赛场地后，未经裁判员允许返回比赛场地内，裁判员必须:</p>
    <p>- 停止比赛(如果该队员没有干扰比赛或裁判员可以掌握有利情形，不必立即停止比赛) </p>
    <p>- 就未经许可进入比赛场地黄牌警告该队员</p>
    <p>- 令该队员离开比赛场地 如果裁判员停止比赛，必须以下列方式重新开始比赛: - 由对方在比赛停止时球所处位置踢间接任意球 </p>
    <p>- 如果队员违反规则第 12 章的规定，按相关处罚条款执行</p>
    <p>- 如果队员越过边界线属于其比赛动作的一部分，则不构成犯规。 </p>
    <p>9. 进球发生时比赛场内有多出的人 </p>
    <p>如果在进球后、重新开始比赛前，裁判员意识到进球时场上多出一人:在下列情况下，裁判员必须判进球无效: </p>
    <p>·多出的人是进球方的队员、替补队员、被换下队员、被罚令出场的队员 或球队官员</p> 
    <p>·多出的人是场外因素，且其干扰了比赛。除非属于本章第 7 条“比赛场内多出的人”中所描述的情形，则进球有效</p>
    <p>比赛应以球门球、角球或坠球重新开始。 - 在下列情况下，裁判员必须判进球有效:</p>
    <p>·多出的人是被进球一方的队员、替补队员、被换下队员、被罚令出场的 队员或球队官员 </p>
    <p>·多出的人是场外因素，且其并未干扰比赛无论何种情况，裁判员必须令多出的人离开比赛场地。如果在进球后，比赛已重新开始，裁判才意识到进球发生时比赛场地上有一个多出的人，进球不可以被取消。如果多出的人仍然在比赛场地，裁判员必须:</p>
    <p>- 停止比赛 </p>
    <p>- 令多出的人离开比赛场地</p>
    <p>- 视情形以坠球或者任意球重新开始比赛 </p>
    <p>- 裁判员必须向相关机构报告该等事件。</p>
    <p> 10. 球队队长 球队队长没有特殊的地位和特权，但他对本队的行为负有一定程度的责
    任。</p>

    <h5>第4章 队员装备</h5> 
    <p>1. 安全性 </p>
    <p>球员不得使用或穿戴有危险性的装备或物品。禁止队员佩戴任何饰物(项链、戒指、手镯、耳环、皮带、橡胶带等)，
    并且不允许用胶带等类似物品对饰物进行包裹或遮挡，必须令其摘除。队员、替补队员在赛前或替换入场前都必须进行装备检查，若其使用或穿戴未经许可的/有危险性的装备或饰物，裁判员必须: 
    <p>- 令其去除该装备或饰物 </p>
    <p>- 如果该队员不能或不愿意这样做，则在下一次比赛成死球状态时令其离开比 赛场地 </p>
    <p>如果队员拒绝遵从，或者再次穿戴违规物件，裁判员必须对其进行黄牌警告。</p>
    <p>2. 必需的装备 </p>
    <p> 队员必需的装备包括以下独立的项目:</p>
    <p>- 有袖上衣、短裤 </p>
    <p>- 护袜——如果使用绷带或其他材料包裹或覆盖，则必须与所包裹或覆盖部分的护袜颜色一致</p>
    <p>- 护腿板——必须由合适的材料制成以提供合理保护，并且必须被护袜全部包住</p>
    <p>- 球鞋</p>
    <p>守门员可以穿着长裤。</p>
    <p>如果一名球员的球鞋或护腿板偶然脱落，他必须在下一次比赛成死球状态前尽可能迅速穿戴好;如果该球员在没来得及这样做之前触球或进球，进球有效。 </p>
    <p>3. 颜色 </p>
    <p>- 每个队的服装颜色必须能够区分彼此和裁判员</p>
    <p>- 每名守门员的服装颜色必须使之能够区别于其他队员和裁判员</p>
    <p>- 如果两名守门员的上衣颜色相同而又都没有其他服装可换，裁判员可以允许 开始比赛。 </p>
    <p>内衣的颜色必须与队服上衣袖子的主色相同;衬裤的颜色必须与队服短裤的主色或者短裤下摆的主色相同。且每支队伍的内衣、衬裤颜色必须统一。</p>
    <p>4. 其他装备 </p>
    <p>无危险的保护装备，例如用柔软、轻便的衬垫材料制成的头盔、面罩、护膝、护臂都是允许使用的，守门员帽子、运动眼镜也是如此。</p>
    <p>如果佩戴头巾或头套，必须符合以下要求:</p>
    <p>- 颜色为黑色或与上衣主色一致(同队队员的颜色必须统一) - 应与队员装备的专业外观相协调</p>
    <p>- 不可与上衣相连接</p>
    <p>- 不会对穿戴者或其他队员构成危险(如开放/闭合式缠绕脖子的结构)</p>
    <p> - 不得有任何部分扩展到表面以外(凸起部分) 不允许在队员之间(包括替补队员、被换下队员和被罚令出场的队员)或
    在队员和技术人员之间进行任何形式的电子通讯。</p>
    <p>在国家足协/赛事组织者同意的前提下，当使用电子表现跟踪系统 (EPTS)时: </p>
    <p>- 它们必须是没有危险性的</p>
    <p>- 在比赛过程中，从该设备或系统中传出的信息和数据不允许在技术区域内接 收或使用，装备中不得有任何涉及政治、宗教或个人的标语、言论和图片。队员不得暴露内衣以显示涉及政治、宗教或个人的标语、言论、图片和广告(制造商商 标除外)。犯有上述违规行为的队员/队伍将被赛事组织者或国家足协处罚，或 由国际足联裁定。 </p>
    <p>5. 违规与判罚 </p>
    <p>对于任何违反本章规则的情形，比赛无须停止，该队员:</p>
    <p>- 在裁判员的要求下离开比赛场地去调整装备 - 在比赛停止时离开比赛场地，除非装备已调整好离场调整或更换装备的队员必须:</p>
    <p>- 在被允许回场前由裁判检查装备</p>
    <p>- 得到主裁判同意后方可重新进入赛场(可以在比赛进行中给出示意)队员未经允许进入比赛场地必须被黄牌警告。如果裁判员为出示黄牌而停止比赛，则在比赛停止时球所在地点罚间接任意球。</p>

    <h5>第 5 章 主裁判 </h5>
    <p>1. 主裁判的权力 每场比赛由一名主裁判控制，他拥有全部权力就该场比赛的相关事宜执行
    竞赛规则。</p>
    <p>2. 主裁判的决定 主裁判应依照竞赛规则和体育精神，尽其最大能力作出判罚。他拥有在竞
    赛规则框架下采取适当行动的自由裁量权，并依其自身判断作出判罚。主裁判根据比赛事实所做出的裁决，包括是否进球和比赛结果，均为最终决定。如果主裁判自己发觉或在接受了来自裁判组其他成员的意见后意识到做出了一个错误的判罚，他不能在比赛重新开始后、在自己已表示半场或全场比赛(包括延长期的比赛)结束并离开比赛场地后、或在比赛终止后，改变其原先的决定。如果主裁判失去执法比赛的能力，比赛可以在裁判组其他成员的监督下继
    续进行直至比赛下一次成死球状态。</p>
    <p>3. 权限和职责 </p>
    <p>主裁判:</p>
    <p> 执行竞赛规则</p>
    <p> 与裁判组其他成员合作控制比赛</p>
    <p> 做好比赛的计时与记录工作，向相关机构提交比赛报告，报告内容包括赛 前、赛中和赛后所有纪律处罚和其他事件的情况</p>
    <p> 管理及/或指示如何重新开始比赛</p>
    有利: 
    <p> 当违例/犯规发生时，如果主裁判认为不判罚对未犯规队伍更有利时，应允许比赛继续进行。如果预期的“有利”在那一瞬间或随后的几秒钟内没有发 生，则判罚最初的犯规 </p>
    <p>纪律处罚: </p>
    <p> 当多种犯规同时发生，根据纪律处罚的程度、重新开始比赛的方式、身体接触的严重性和对战术的影响择重判罚</p>
    <p> 对应给予黄牌警告或罚令出场的队员进行纪律处罚</p>
    <p> 拥有执行纪律处罚的权力，时限从进入赛场(包括赛前检视阶段)到比赛结 束离开比赛场地(包括踢点球决胜阶段)</p>。如果在比赛前未入场时，队员犯有 可被罚令出场的行为，主裁判有权禁止其参加该场比赛(参见竞赛规则第 3 章 第 6 条);主裁判需要报告任何其他不正当行为有权出示黄牌或红牌，时限从比赛开始前进入比赛场地到比赛结束(包括半 场休息、加时赛期间和点球决胜阶段)向对自己行为不负责任的球队官员进行处罚，并可将其驱逐出比赛场地及其 周边区域对于主裁判未看到的事件，可根据裁判组其他成员的建议进行判罚</p>
    <p>受伤: </p>
    <p> 如果队员只是受轻伤，则允许比赛继续进行直到下一次成死球状态</p>
    <p> 如果队员受伤较重，则停止比赛，并确保将其移出比赛场地。受伤队员不能在场内接受治疗，且只有在比赛重新开始后才能重返比赛场地。在比赛进行中只允许从边线进入场内，比赛成死球状态时，则可从球场任何边界线进入场内。下列例外情况可以允许在场内接受治疗:
    <p>·当守门员受伤时 </p>
    <p>·当守门员和其他队员发生碰撞需要立即引起关注时 </p>
    <p>·当同队队员发生碰撞需要立即引起关注时 </p>
    <p>·当发生严重受伤时</p>
    <p>·队员因被对方侵犯而受伤，且犯规队员因此而被黄牌警告或罚令出场(例如 鲁莽或严重的争抢犯规)，该被侵犯队员可以在场内接受适时评估和处理</p> 
    <p> 确保任何流血的队员离开比赛场地。主裁判只有在确认该队员流血被止 住且装备上无血迹后方可给出示意，允许其重新回到比赛场地</p>
    <p> 如果主裁判允许医生/担架员进场，受伤的队员必须离开球场，不管是用担架 抬出还是自行走出，否则将因非体育行为而被黄牌警告</p>
    <p> 如果主裁判决定对一名已经受伤并需离开比赛场地接受治疗的队员进行黄牌 警告或罚令出场，必须在该队员离开场地前出牌示意</p>
    <p> 如果比赛不是因其他理由而暂停，或者队员的受伤不是因违反竞赛规则造成 的，比赛以坠球形式重新开始 </p>
    外来干扰:
    <p> 因违背规则或外来干扰而停止、暂停或终止比赛，例如: ·赛场照明灯光不足 </p>
    <p> 观众掷入场内的物品击中了裁判、队员或球队官员，主裁判根据事故的严重 程度可以令比赛继续、暂停或终止</p>
    <p> 观众鸣哨并干扰了比赛，须停止比赛，以坠球形式重新开始比赛 
    <p> 在比赛进行中，多余的球、其他物品或动物进入比赛场地，
    主裁判必须仅在其干扰比赛的情况下，停止比赛(以坠球形式重新开始)。如果干扰发生在球进入球门过程中，且该干扰没有阻碍防守队员触球，而球最终 进入球门(即便此干扰接触到了球)，判进球有效，除非球进入的是干扰者对 方的球门在其未干扰比赛的情况下允许比赛继续，并尽早找机会将其移出 - 确保没有未经允许的人员进入比赛场地</p>
    <p>4、主裁判的装备</p>
    <p>必需的装备: 哨、手表、红黄牌、记录本(或用来记录比赛的其他工具) </p>
    <p>其他装备: </p>
    <p>主裁判可以被准许使用:</p>
    <p>- 和裁判组其他成员沟通的装备(如蜂鸣器/BP 旗，耳麦等)</p>
    <p>- 电子表现跟踪系统(EPTS)或其他健康监测系统 主裁判和其他裁判组成员禁止佩戴饰物和其他电子设备。</p>
