    <p>这些决定可以包括:</p> 
    <p>- 根据比赛场地及其周边情况，或天气的影响决定比赛是否进行</p> 
    <p>- 由于各种原因中止比赛</p>
    <p>- 选择适合比赛场地使用的器材和比赛球</p>
    <p>- 由于观众的干扰或者观众席中的任何问题决定是否停止比赛</p>
    <p>- 是否停止比赛以便受伤队员移出比赛场地接受治疗</p>
    <p>- 要求将受伤队员移出比赛场地接受治疗</p>
    <p>- 是否允许队员可以穿着某种服装或佩戴某种装备</p>
    <p>- 在主裁判职责范围内，决定是否允许任何人(包括球队或体育场管理人员、 安保人员、摄影记者或其他媒体代表)出现在比赛场地附近</p>
    <p>- 根据竞赛规则，或者按照比赛所适用的国际足联、洲际足联、国家足协规 则，或赛事规则或规程赋予其的职责而做出的任何其他决定</p>

    <h5>第 6 章 其他比赛官员 </h5>
    <p>(译者注:本章中提及比赛官员均不包括裁判员，虽然裁判员被视为最重要的比赛官员)</p>
    <p>每场比赛可以指派其他比赛官员(两名助理裁判员，第四官员，两名附加助理裁判员，替补助理裁判员)。他们根据竞赛规则协助裁判员控制比赛，但由裁判员做出最终决定。比赛官员在裁判员的指导下工作，如有过分干预比赛或不合适的表现时，裁判员可解除其职责并将报告提交相关机构。比赛官员(不含替补助理裁判员)在其视线比裁判员更好的情况下协助裁判员判罚犯规和违规情况，并将发生在裁判员和其余比赛官员视野外的任何严重的不正当行为或其他事件提交报告至相关机构。同时必须将所写报告内容通报裁判员和其余比赛官员。</p>
    <p>比赛官员通过以下形式协助裁判员:1检查比赛场地和用球，检查并纠正球员装备。2记录比赛时间、进球和不正当行为。</p>
    <p>竞赛规则必须明确当一名比赛官员因故不能开始或继续工作时的替换人选与相关改变。特别要明确的是，在裁判员不能继续担任临场工作的情况下，应由第四官员还是第一助理裁判员还是第一附加助理裁判员担任裁判员。</p>
    <p>1、助理裁判员</p>
    <p>助理裁判员的职责为示意:</p>
    <p>• 当球的整体离开比赛场地时，应由哪一队踢角球、球门球或掷界外球。</p> 
    <p>• 球员处于越位位置并犯规。</p>
    <p>• 要求替换队员。 </p>
    <p>• 罚球点球时，守门员在球被踢之前移出球门线，以及球是否越过球门 线。如果已指派附加助理裁判员，助理裁判员应与点球点齐平。 
    助理裁判员还需协助监督替换程序。 助理裁判员可以进入赛场内协助裁判员控制好 9.15 米(10 码)的距离。 </p>
    <p>2、第四官员</p>
    <p>第四官员的协助内容:</p>
    <p>• 监督替换程序。</p>
    <p>• 检查队员和替补队员的装备。</p>
    <p>• 队员在得到裁判员的信号或允许后重新入场。</p>
    <p>• 管理比赛备用球。</p>
    <p>• 在每个半场比赛(包括加时赛)结束前展示裁判员决定的最少补时时间。</p>
    <p>• 将技术区域内任何人的不负责任的行为通知裁判员。</p> 
    <p>3、附加助理裁判员</p>
    <p>附加助理裁判员的协助内容:</p>
    <p>• 当球的整体越过球门线(包括进球有效)。</p>
    <p>• 哪一队踢角球或球门球。</p>
    <p>• 罚球点球时，守门员在球被踢之前移出球门线，以及球是否越过球门线。
    <p>4、替补助理裁判员 替补助理裁判员的唯一职责是替换不能继续执法的助理裁判员或第四官员。</p>
